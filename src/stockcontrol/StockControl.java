    private void quantityKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_quantityKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        Double itemCost = 0.0;
        double totalCst=0;
        
        
        if(!Character.isDigit(c)) { //for get only numeric input
            evt.consume();
                        
            
            if(productCost.getText()!=null)
            itemCost = new Double(Double.parseDouble(productCost.getText()));

            if(quantity.getText()!=null){
                totalCst = itemCost*Double.parseDouble(quantity.getText());
                totalCost.setText(String.valueOf(totalCst));//set total cost text area
            }
        }
        
    }//GEN-LAST:event_quantityKeyTyped

