    private void checkItemCritical() {
        if(Integer.parseInt(stockLevel.getText())<=Integer.parseInt(stockCritical.getText()))
            {
                warning.setText("Selected Item at critical level !!");    //if critical set background color red               
            }
            else
            {
                warning.setText("No warning.");
            }
    }
    
    //get item attributes by id
    private void setItemsAttributes(String ID) {
        
        ResultSet result = null;
        
        result = scConnection.getItemByID(ID);
         
            try {
                while(result.next()){
                    productName.setText(result.getString("name"));
                    productDesc.setText(result.getString("description"));
                    stockLevel.setText(result.getString("stocklevel"));
                    stockCritical.setText(result.getString("criticallevel"));
                    productCost.setText(result.getString("cost"));
                    if(stockLevel.getText()!=null)
                        checkItemCritical();    //check critical level
                }
            } catch (SQLException ex) {
                Logger.getLogger(StockControl.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }

    public StockControl(String username) {
        
        //Get current date
        SimpleDateFormat formatter= new SimpleDateFormat("yyyy-MM-dd 'at' HH:mm:ss z");
        Date date = new Date(System.currentTimeMillis());
        
        //get username
        this.username = username;   //comes from login class
        
        scConnection = new ConnectionDB();//get reference for db
        
        initComponents();
        
        
        //set component
        userInformation.setText(username); 
        currentDate.setText(formatter.format(date));
        
        items = scConnection.getItemsByID();    //get items to combobox
        if(items != null) {
            productID.removeAllItems();
            
            for(i=0;i<items.length;i++) {
                productID.addItem(items[i]);                
            }
            
            i=0;
        }
        String ID = productID.getSelectedItem().toString(); //get selected item
        if( !ID.equals(null)){
            setItemsAttributes(ID);
            initCompleted = true;
        }
        
    }
    

    
