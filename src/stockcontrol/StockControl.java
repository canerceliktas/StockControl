    private void exitProgramActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exitProgramActionPerformed
        // TODO add your handling code here:
        Double itemCost = 0.0;
        double totalCst=0;
        if(productCost.getText()!=null)
            itemCost = new Double(Double.parseDouble(productCost.getText()));

        if(quantity.getText()!=null){
            totalCst = itemCost*Double.parseDouble(quantity.getText());
            totalCost.setText(String.valueOf(totalCst));//set total cost text area
        }
        
        if(CheckInvalidStock())
        {
            //Show popup about order information
            JOptionPane.showMessageDialog(null, "The Operation succesfull ! \n Company : "+ customerName.getText()+
                "\n Quantity : "+quantity.getText()+"\n Total Price : "+totalCost.getText()+" TRY \n Address : "
                +address.getText());
        
            //Decrease the item's stocklevel
            scConnection.decreaseProduct(productID.getSelectedItem().toString(), 
            Integer.parseInt(stockLevel.getText()) , Integer.parseInt(quantity.getText()));
        }
        
    }//GEN-LAST:event_exitProgramActionPerformed

