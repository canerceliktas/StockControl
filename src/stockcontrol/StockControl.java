    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        // TODO add your handling code here:
        Object curentItem = productID.getSelectedItem();    //Current Item
        
        productID.removeAllItems(); //clear combobox
        items = null;                           //clear itemlist
        items = scConnection.getItemsByID();    //get items to combobox
        if(items != null) {
            
            for(i=0;i<items.length;i++) {       //fill the combobox
                productID.addItem(items[i]);                
            }
            
            i=0;
        }
        productID.setSelectedItem(curentItem);
        String ID = productID.getSelectedItem().toString(); //get selected item
        if( !ID.equals(null)){
            setItemsAttributes(ID);   
            checkItemCritical();            
        }
    }//GEN-LAST:event_refreshButtonActionPerformed

