    private boolean CheckInvalidStock() {
        int orderQuantity = 0;
        int currentStockLevel = 0;
        
        if(quantity.getText()!= null)
            orderQuantity = Integer.parseInt(quantity.getText());
        
        if(stockLevel.getText()!= null) 
            currentStockLevel = Integer.parseInt(this.stockLevel.getText());
        
        if(currentStockLevel-orderQuantity<0)
        {
            JOptionPane.showMessageDialog(null, "Not enough item !");
            return false;
        }
        else 
        {
            return true;
        }
        
        
    }
